nome = "Sabrina Ramos Prado"

print(nome[0])#letra na posição 0
print(nome[:9])#do começo ao caracter 9
print(nome[10:])#a partir do caracter 10
print(nome[10:16])#do caracter 10 ao 16
print(nome[10:16:2])#do caracter 10 ao 16 pulando de 2 em 2
print(nome[:])#sem começo e sem fim, ou seja, tudo
print(nome[::-1])#de trás par a frente, ou seja, um espelhamento da string inicial
print(nome[-1])#funciona com números negativos, faz com que pegue os caracteres de trás para a frente